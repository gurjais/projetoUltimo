    public void adicionarLista(){
        DefaultTableModel model;
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
        EntityManager em = factory.createEntityManager();
        
        Query q = em.createNamedQuery("Encomenda.findAll");
        for (Object d : q.getResultList()) {
            if ((((Encomenda) d).getCodEncomenda()!= null)) {
                 model.addRow(new Object[]{((Encomenda) d).getCodEncomenda(),((Encomenda) d).getIdFuncionario().getIdFuncionario(),((Encomenda) d).getCodFornecedor().getCodFornecedor(),((Encomenda) d).getDataEncomenda(),((Encomenda) d).getPrecototal()});
            }
        }
    }

